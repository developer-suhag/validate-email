const emailField = document.getElementById('email-field');
const validationMessage = document.getElementById('validation-message');

const showMessage = (message, cls) => {
    validationMessage.classList.add(cls)
    validationMessage.innerText = `${message}`;
}


document.getElementById('check-btn').addEventListener('click', () => {
    const email = emailField.value;
    if (email) {
        checkEmail(email);
    } else {
        showMessage('Please type your email address', 'text-warning')
    }
    emailField.value = '';
});

const checkEmail = email => {
    const result = emailValidation.test(`${email}`);

    // show message 
    if (result) {
        showMessage('✔️ Your Email Address is valid', 'text-success')
    } else {
        showMessage('❌ Opps your Email Address is not valid', 'text-danger')
    }
}